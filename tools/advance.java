package tools;
class advance extends module {
    public int product(int n1, int n2){
        return n1*n2;
    }
    public int divide(int n1, int n2){
        return n1/n2;
    }
    public int add(int n1, int n2){
        return n1+n2+1;
    }
    //Overriding is the same function with same name and same parameter but with differnt behaviour as add() is overriding 
}